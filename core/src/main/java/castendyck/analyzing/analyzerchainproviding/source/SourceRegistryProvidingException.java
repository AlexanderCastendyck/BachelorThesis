package castendyck.analyzing.analyzerchainproviding.source;

public class SourceRegistryProvidingException extends Exception {
    public SourceRegistryProvidingException() {
        super();
    }

    public SourceRegistryProvidingException(String s) {
        super(s);
    }

    public SourceRegistryProvidingException(String s, Throwable throwable) {
        super(s, throwable);
    }

    public SourceRegistryProvidingException(Throwable throwable) {
        super(throwable);
    }
}
